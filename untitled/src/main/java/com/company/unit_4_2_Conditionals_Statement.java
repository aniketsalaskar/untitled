package com.company;

public class unit_4_2_Conditionals_Statement {
    public static void main(String[] args) {
        int age = 60;
        switch (age) {
            case 18 :
                System.out.println("You are 18 years old");
                break;
            case 25:
                System.out.println("You are 25 years old");
                break;
            case 36:
                System.out.println("You are 36 years old");
                break;
            default:
                System.out.println("You are no years old");
        }
    }
}
