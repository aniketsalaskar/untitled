package com.company;

public class unit_2_resulting_datatypes {
    public static void main(String[] args){

        int a = 5;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);


        // Quick Query
        System.out.println("***Quick Query***");
        int y = 7;
        int x = ++y * 8;
        System.out.println(x);
        char ch = 'a';
        ch++;
        System.out.println(ch);

    }
}
